enviarse a Athletic Depor S.A.C., persona jurídica de la marca Runa Store, una notificación de incumplimiento.


La empresa entrega únicamente a los distritos listadas en el proceso de compra al seleccionar su distrito
. Una vez que el operador logístico retire su pedido del local le notificaremos de esto con un email de
