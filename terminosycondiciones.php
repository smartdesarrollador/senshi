 del proveedor asignado.

Por política del Courier de entregas y la empresa, la persona que recepciona el pedido deberá presentar su DNI al momento de la entrega del pedido.

Athletic Depor S.A.C., persona jurídica de la marca Runa Store, realizará la entrega en la dirección provista por el cliente dentro del territorio peruano. La empresa sólo puede realizar la entrega en un domicilio residencial o de oficina en que haya ocupantes que puedan recibir la mercadería. Las entregas se realizarán en días hábiles (lunes a viernes), excepto los días festivos. Todas las entregas irán acompañadas de un acuse de recibo. El horario de entregas es de 9:00am a 7:00pm, en tiempos de campaña como los Cybers los pedidos podrían entregarse después de las 8:00pm.

runastore.pe entrega únicamente a las ciudades listadas en el proceso de compra al seleccionar su distrito. 

