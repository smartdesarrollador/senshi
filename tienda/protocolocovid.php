       <div class="row" > 
          <div class=" col-sm-12 col-md-12 col-lg-12 col-xl-12 mt-5" >
              <h2 style="text-align: center; color:#ffffff;font-size: 30px"> SENSHI SUSHI/BAR</h1>
               <h4 class="text-justify">Protocolo de bioseguridad que deberán cumplir los 
               restaurantes para operar vía delivery
               </h4>   
                  <br>
              <p>Documento elaborado por el Mincetur-Ministerio de Salud</p>
               <p class="text-justify">Los restaurantes deberán cumplir con un riguroso protocolo de higiene, salubridad y seguridad
               sanitaria.</p>
               <p class="text-justify">¿En qué consiste este protocolo de bioseguridad frente al Covid-19? ,
                elaborado por el Ministerio de Comercio Exterior y Turismo (Mincetur), Cenfotur y el
                 Ministerio de la Producción en coordinación con la CCL, Ahora y la ONG Puca.</p>
                  
                  <h5>ASPECTOS GENERALES DEL SERVICIO</h5> 
                 <p class="text-justify">Las empresas deberán atender pedidos en los horarios establecidos por las autoridades; 
                  recibir órdenes por teléfono, whastApp o Apps de cada marca, más no en el restaurante; 
                  permanecer con los locales cerrados al público y funcionar solo con las áreas de producción,
                   despacho, facturación y reparto.</p>

                 <h5>CUIDADOS DEL PERSONAL</h5>
                 <p class="text-justify">Contempla que el personal operativo, administrativo, de reparto y externos 
                 (proveedores, fiscalizadores, personal de mantenimiento) deberá contar con los equipos de 
                 protección (EP) establecidos como guantes, cofia, lentes de protección y mascarilla, 
                 según la actividad que cada colaborador realice.</p>

                 <p class="text-justify">Cómo ya se ha puesto en práctica en algunos establecimientos comerciales, 
                 los negocios gastronómicos deberán tomar la temperatura a sus colaboradores antes de ingresar a 
                 trabajar; entre otras medidas como la desinfección de zapatos, cambio de indumentaria, etc.
                 Se recomienda, incluso, que la empresa acondicione duchas.
                 Los guantes y mascarillas deberán ser cambiadas cada cuatro horas o antes si su estado lo amerita. 
                 El uso de este material no exime el lavado constante de las manos y la utilización de desinfectantes.</p>
                 <h5>EN LAS INSTALACIONES</h5>
                 <p class="text-justify"> Para que chifas, pollerías, ‘fast foods’, cafeterías, entre otros, inicien operaciones tendrán que,
                  previamente,hacer un saneamiento ambiental de sus locales (desratización, desinsectación y
                   desinfección). Además, se exige una limpieza constante y meticulosa en todos los ambientes de la tienda, 
                  por ello es indispensable tener siempre los productos para ello y el suministro de agua.</p>
                 <h5>MEDIDAS DE CONTROL POR PROCESOS</h5>
                  <p class="text-justify">Las medidas de bioseguridad se tomarán en cuenta en todos los procesos 
                   operativos como la actividad de compra, recepción, almacenamiento y acondicionamiento de los 
                   alimentos.En cuanto a la preparación de pedidos, despacho y reparto el protocolo es igual de exigente.
                   En estos casos, los responsables de cada restaurante deberán verificar que los encargados antes, 
                   durante y después de cada actividad se laven y desinfecten las manos; que cuenten con el equipo de
                   protección respectivo; que las áreas y equipos de acondicionamiento de alimentos se encuentren limpias 
                   y desinfectadas; que se disponga de desinfectantes para equipos y materiales a utilizar; que se respete el 
                   distanciamiento social de al menos un metro, entre muchos otros.</p>
                   La entrega de pedidos al cliente en el restaurante, ya sea de una orden de pollo a la brasa, pizza o
                   de un chaufa con tallarín saltado, se realizará en una zona externa, respetando siempre el
                   metro de distancia entre personas. El despachador tampoco podrá hacer las veces de cajero, de esto 
                   deberá encargarse otra persona.La orden, además, se deberá embalar en un envase secundario y se le 
                   colocará un precinto se seguridad para evitar,sobre todo, la manipulación durante el traslado al
                    domicilio, en caso sea un pedido por delivery.El personal que realice el reparto deberá desinfectar 
                    su vehículo y el espacio donde se colocará el pedido, planificar su ruta de entrega para evitar 
                    aglomeraciones y evitar en todo momento mantener el metro de distancia con el comensal, si es en un 
                    edifico pues lo dejará en recepción. Asimismo, el uso de desinfectantes es constante.</p>
                    <h5>MEDIOS DE PAGO</h5>
                    <p class="text-justify">Se insta a que los clientes opten por medios de pago electrónico para evitar mayor contacto y reducir el riesgo 
                    de contagio del coronavirus. Si paga en efectivo el vuelt
                    se entregará empaquetado, como ya venían haciendo algunas cadenas antes de la pandemia incluso. Para ello, las
                    empresas deberán gestionar campaña de difusión por medios digitales sobre cómo prevenir el contagio a fin de
                    sensibilizar a los clientes.
                    Sin duda, los restaurantes pequeños, medianos y grandes tendrán un arduo trabajo para adaptarse a 
                    este protocolo y a este nuevo escenario que impone no solo nuevos hábitos de higiene y
                    salubridad, sino también de un presupuesto aparte. La pregunta que ahora queda en el aire es
                     ¿cuántos podrán cumplirlo?</p>

